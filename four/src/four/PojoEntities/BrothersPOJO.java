package four.PojoEntities;

public class BrothersPOJO {

	String name;
	int age;
	int impl;
	String implePlace;
	String merryTo;
	String MehutanimCity;

	
	public BrothersPOJO(String name, int age, int impl, String implePlace,
			String merryTo, String mehutanimCity) {
		
		super();
		this.name = name;
		this.age = age;
		this.impl = impl;
		this.implePlace = implePlace;
		this.merryTo = merryTo;
		MehutanimCity = mehutanimCity;
	}
	
	
	
}
